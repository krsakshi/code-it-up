#  Question : Program to find the sum of all numbers stored in a list
#        Input : List of numbers
#        Output : Sum of all the elements in the list

# List of numbers
numbers = [6, 5, 3, 8, 4, 2, 5, 4, 11]

# variable to store the sum
sum = 0

# iterate over the list
for val in numbers:
    sum = sum+val

print("The sum is", sum)