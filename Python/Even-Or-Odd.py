# Question : Tocheck wether the number is even or odd 
#       Input : User input a number
#        Output : natural numbers

N=int(input("Enter a number : "))
print("\n THE ENTERED NUMBER IS  :\n",N)
if (N%2==0):
    print("\n The entered number is Even.\n")
else:
    print("\n The entered number is Odd.\n")

